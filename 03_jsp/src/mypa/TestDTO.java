package mypa;

public class TestDTO {
	//전역변수 = 프로퍼티 = property
	private String id;
	private String pw;
	private String name;
	
	public TestDTO(){
		
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getPw() {
		return pw;
	}

	public void setPw(String pw) {
		this.pw = pw;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
}
